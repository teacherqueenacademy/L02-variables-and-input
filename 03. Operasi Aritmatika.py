#  Python memiliki operator matematika dasar:

# + (tambah)
# - (kurang)
# * (kali)
# / (bagi, hasil desimal)
# // (bagi, hasil bulat)
# % (modulus, sisa bagi)

# Penjumlahan (+)
hasil_tambah = 10 + 5
print(hasil_tambah)  # Output: 15

# Pengurangan (-)
hasil_kurang = 10 - 3
print(hasil_kurang)  # Output: 7

# Perkalian (*)
hasil_kali = 4 * 3
print(hasil_kali)  # Output: 12

# Pembagian (/) -> Hasil desimal
hasil_bagi = 10 / 4
print(hasil_bagi)  # Output: 2.5

# Pembagian bulat (//) -> Hasil dibulatkan ke bawah
hasil_bagi_bulat = 10 // 4
print(hasil_bagi_bulat)  # Output: 2

# Modulus (%) -> Sisa pembagian
hasil_modulus = 10 % 4
print(hasil_modulus)  # Output: 2

