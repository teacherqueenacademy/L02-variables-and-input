"Type Data"
# ✅ Integer (bilangan bulat)
# ✅ Float (bilangan desimal)
# ✅ String (teks)
# ✅ Boolean (benar/salah)
# ✅ List (kumpulan data)

# 1️⃣ Integer (int) → Bilangan bulat seperti 10, -5, 1000
umur = 12
print(umur)  # Output: 12

# 2️⃣ Float → Bilangan desimal seperti 3.14, 0.5, -2.75
tinggi = 165.5
print(tinggi)  # Output: 165.5

# 3️⃣ String (str) → Teks, harus ditulis dalam tanda kutip
nama = "Aldi"
print(nama)  # Output: Aldi

# 4️⃣ Boolean (bool) → True (benar) / False (salah)
lulus = True
print(lulus)  # Output: True

# 5️⃣ List → Kumpulan beberapa data dalam [ ]
buah = ["Apel", "Jeruk", "Mangga"]
print(buah)  # Output: ['Apel', 'Jeruk', 'Mangga']


